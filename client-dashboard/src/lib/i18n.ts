export type Language = 'pl' | 'en'
export type TranslationKey = keyof typeof translations.pl

export const getTranslation = (lang: Language, key: string): string => {
  const keys = key.split('.')
  let value: any = translations[lang]
  
  for (const k of keys) {
    value = value?.[k]
  }
  
  return value || key
}